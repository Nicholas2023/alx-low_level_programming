/**
 * add - Adds two integers
 * @a: Integer 1
 * @b: Integer 2
 *
 * Return: Sum of a and b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Finds the difference of two integers
 * @a: Integer 1
 * @b: Integer 2
 *
 * Return: Difference of two integers
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiplies two integers
 * @a: Integer 1
 * @b: Integer 2
 *
 * Return: Product of two integers
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divides two integers
 * @a: Integer 1
 * @b: Integer 2
 *
 * Return: Quotient of two integers
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Finds the modulus of two integers
 * @a: Integer 1
 * @b: Integer 2
 *
 * Return: Modulus of two integers
 */

int mod(int a, int b)
{	
	return (a % b);
}
